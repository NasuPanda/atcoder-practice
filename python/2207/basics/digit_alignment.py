# :の右側が書式指定文字列
# :の左側がformat関数の引数のインデックス
a = 1.0
b = 3.1415
c = 1.41421356

# formatの場合
print("{:.5f}".format(c))                          # 1.41421
print("{0} {1} {2:.5f} {2:.6f}".format(a, b, c))   # 1.0 3.1415 1.41421 1.414214

# f文字列の場合
print(f"{c:.5f}")                 # 1.41421
print(f"{a} {b} {c:.5f} {c:.6f}") # 1.0 3.1415 1.41421 1.414214